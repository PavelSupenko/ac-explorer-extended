class Plugin(BasePlugin):
	plugin_name = 'Save Skeleton Hierarchy'
	plugin_level = 1
	dev = True

	def run(self, file_id: Union[str, int], forge_file_name: str, datafile_id: int, options: Union[List[dict], None] = None):
		if os.path.isfile(os.path.join(os.path.dirname(__file__), 'bone_hierarchy.json')):
			with open(os.path.join(os.path.dirname(__file__), 'bone_hierarchy.json')) as f:
				bone_hierarchy: Dict[str, List[Union[str, None]]] = json.load(f)
		else:
			bone_hierarchy: Dict[str, List[Union[str, None]]] = {}

		for file_id, forge_file_name, datafile_id in skeles:
			file_id = struct.unpack('<Q', binascii.unhexlify(file_id))[0]
			datafile_id = struct.unpack('<Q', binascii.unhexlify(datafile_id))[0]

			data = pyUbiForge.temp_files(file_id, forge_file_name, datafile_id)
			if data is None:
				logging.warning(f"Failed to find file {file_id:016X}")
				return
			if data.file_type != "24AECB7C":
				continue

			skeleton: Skeleton = pyUbiForge.read_file(data.file)
			if skeleton is None:
				print(f'failed reading file {file_id}')
				continue

			file_id_to_bone_id = {bone.file_id: bone.bone_id for bone in skeleton.bones}

			for bone in skeleton.bones:
				# bone: Bone
				if bone.parent in file_id_to_bone_id:
					bone_hierarchy.setdefault(bone.bone_id, [])
					if file_id_to_bone_id[bone.parent] not in bone_hierarchy[bone.bone_id]:
						bone_hierarchy[bone.bone_id].append(file_id_to_bone_id[bone.parent])
				else:
					bone_hierarchy.setdefault(bone.bone_id, [])
					if None not in bone_hierarchy[bone.bone_id]:
						bone_hierarchy[bone.bone_id].append(None)

		with open(os.path.join(os.path.dirname(__file__), 'bone_hierarchy.json'), 'w') as f:
			json.dump(bone_hierarchy, f)


